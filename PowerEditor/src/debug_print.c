int print_capture(BYTE *keys,char *str,int len)
{
	int i,count=0,max=4;
	str[0]=0;
	for(i=0;i<256;i++){
		if(keys[i]!=0){
			if(count!=0)
				strcat(str,"+");
			_snprintf(str+strlen(str),len-strlen(str),"%s",virtual_keys[i]);
			count++;
			if(count>=max)
				break;
		}
	}
	return TRUE;
}
