char *strstri(char *src,char *sub)
{
	int index=0,sub_index=0;
	char a;
	char *start=0;
	while((a=src[index])!=0){
		char b;
		a=tolower(a);
		b=sub[sub_index];
		if(0==sub_index)
			start=src+index;
		b=tolower(b);
		if(0==b)
			return start;
		if(a==b){
			sub_index++;
		}else{
			sub_index=0;
		}
		index++;
	}
	if(sub_index>0){
		if(0==sub[sub_index])
			return start;
	}
	return 0;
}
